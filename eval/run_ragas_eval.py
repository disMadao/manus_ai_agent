#!/usr/bin/env python3
"""Run RAGas evaluation for LoveApp diary RAG flow.

This script is intentionally standalone from Maven and can run with mocked
or real backend answers. By default it calls:
  GET /api/ai/love_app/chat/sync?message=...&chatId=...
"""

from __future__ import annotations

import argparse
import csv
import json
import math
import sys
from dataclasses import dataclass
from datetime import datetime
from pathlib import Path
from typing import Any

import httpx
import yaml
from datasets import Dataset
from ragas import evaluate
from ragas.metrics import (
    answer_relevancy,
    context_precision,
    context_recall,
    faithfulness,
)


@dataclass
class EvalSample:
    sample_id: str
    question: str
    reference_answer: str
    reference_contexts: list[str]
    tags: list[str]
    dataset_version: str


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run LoveApp RAGas evaluation")
    parser.add_argument(
        "--base-url",
        default="http://localhost:8123/api",
        help="Backend base URL, default: http://localhost:8123/api",
    )
    parser.add_argument(
        "--dataset",
        default="eval/datasets/loveapp_ragas_v1.jsonl",
        help="Path to JSONL dataset",
    )
    parser.add_argument(
        "--thresholds",
        default="eval/config/thresholds.yaml",
        help="Path to thresholds yaml",
    )
    parser.add_argument(
        "--output-dir",
        default="eval/results",
        help="Directory to write evaluation outputs",
    )
    parser.add_argument(
        "--gate",
        action="store_true",
        help="Enable quality gate check",
    )
    parser.add_argument(
        "--captured-jsonl",
        default="",
        help=(
            "Optional path to captured JSONL generated by Java test "
            "(e.g. target/ragas/<ts>/loveapp_eval_capture.jsonl). "
            "When provided, script evaluates this file directly instead of calling backend."
        ),
    )
    parser.add_argument(
        "--timeout",
        type=float,
        default=60.0,
        help="HTTP timeout per request in seconds",
    )
    return parser.parse_args()


def load_samples(dataset_path: Path) -> list[EvalSample]:
    samples: list[EvalSample] = []
    with dataset_path.open("r", encoding="utf-8") as f:
        for idx, line in enumerate(f, start=1):
            line = line.strip()
            if not line:
                continue
            data = json.loads(line)
            for required in ("id", "question", "reference_answer", "reference_contexts"):
                if required not in data:
                    raise ValueError(f"Dataset line {idx} missing field: {required}")
            samples.append(
                EvalSample(
                    sample_id=str(data["id"]),
                    question=str(data["question"]),
                    reference_answer=str(data["reference_answer"]),
                    reference_contexts=[str(x) for x in data.get("reference_contexts", [])],
                    tags=[str(x) for x in data.get("tags", [])],
                    dataset_version=str(data.get("dataset_version", "v1")),
                )
            )
    if not samples:
        raise ValueError("Dataset is empty")
    return samples


def load_thresholds(threshold_path: Path) -> dict[str, float]:
    with threshold_path.open("r", encoding="utf-8") as f:
        raw = yaml.safe_load(f) or {}
    expected_keys = [
        "faithfulness",
        "answer_relevancy",
        "context_precision",
        "context_recall",
    ]
    for key in expected_keys:
        if key not in raw:
            raise ValueError(f"Threshold missing key: {key}")
    return {k: float(raw[k]) for k in expected_keys}


def call_love_app_sync(
    client: httpx.Client,
    base_url: str,
    question: str,
    sample_id: str,
) -> tuple[str, list[str], str | None]:
    endpoint = f"{base_url.rstrip('/')}/ai/love_app/chat/sync"
    chat_id = f"ragas-{sample_id}"
    params = {"message": question, "chatId": chat_id}

    resp = client.get(endpoint, params=params)
    resp.raise_for_status()

    # Current API returns plain text answer.
    answer = resp.text.strip()

    # No official retrieved_contexts field currently. Keep explicit marker.
    retrieved_contexts: list[str] = []
    missing_reason = "backend_response_has_no_retrieved_contexts"

    return answer, retrieved_contexts, missing_reason


def safe_float(value: Any) -> float | None:
    if value is None:
        return None
    try:
        v = float(value)
    except (TypeError, ValueError):
        return None
    if math.isnan(v) or math.isinf(v):
        return None
    return v


def read_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open("r", encoding="utf-8") as f:
        for line_no, line in enumerate(f, start=1):
            raw = line.strip()
            if not raw:
                continue
            try:
                row = json.loads(raw)
            except json.JSONDecodeError as ex:
                raise ValueError(f"Invalid JSONL at line {line_no}: {ex}") from ex
            rows.append(row)
    return rows


def main() -> int:
    args = parse_args()

    dataset_path = Path(args.dataset)
    thresholds_path = Path(args.thresholds)
    output_root = Path(args.output_dir)
    captured_jsonl = Path(args.captured_jsonl) if args.captured_jsonl else None

    if not dataset_path.exists():
        print(f"[ERROR] Dataset not found: {dataset_path}", file=sys.stderr)
        return 2
    if not thresholds_path.exists():
        print(f"[ERROR] Threshold config not found: {thresholds_path}", file=sys.stderr)
        return 2

    samples = load_samples(dataset_path)
    thresholds = load_thresholds(thresholds_path)

    run_dir = output_root / datetime.now().strftime("%Y%m%d-%H%M%S")
    run_dir.mkdir(parents=True, exist_ok=True)

    questions: list[str] = []
    answers: list[str] = []
    contexts: list[list[str]] = []
    reference_answers: list[str] = []
    reference_contexts: list[list[str]] = []

    per_sample_rows: list[dict[str, Any]] = []

    failures = 0
    sample_by_id = {s.sample_id: s for s in samples}

    if captured_jsonl:
        if not captured_jsonl.exists():
            print(f"[ERROR] Captured JSONL not found: {captured_jsonl}", file=sys.stderr)
            return 2
        captured_rows = read_jsonl(captured_jsonl)
        if not captured_rows:
            print(f"[ERROR] Captured JSONL is empty: {captured_jsonl}", file=sys.stderr)
            return 2

        for row in captured_rows:
            sample_id = str(row.get("id", ""))
            sample = sample_by_id.get(sample_id)
            if sample is None:
                failures += 1
                continue

            answer = str(row.get("answer", ""))
            retrieved_contexts = [str(x) for x in row.get("contexts", []) if x is not None]
            effective_contexts = retrieved_contexts if retrieved_contexts else sample.reference_contexts
            context_mode = "retrieved" if retrieved_contexts else "reference_fallback"
            missing_reason = "captured_contexts_empty" if context_mode == "reference_fallback" else ""

            questions.append(sample.question)
            answers.append(answer)
            contexts.append(effective_contexts)
            reference_answers.append(sample.reference_answer)
            reference_contexts.append(sample.reference_contexts)

            per_sample_rows.append(
                {
                    "id": sample.sample_id,
                    "question": sample.question,
                    "answer": answer,
                    "reference_answer": sample.reference_answer,
                    "retrieved_contexts_count": len(retrieved_contexts),
                    "effective_contexts_count": len(effective_contexts),
                    "context_mode": context_mode,
                    "context_missing_reason": missing_reason,
                    "tags": "|".join(sample.tags),
                    "dataset_version": sample.dataset_version,
                }
            )
    else:
        with httpx.Client(timeout=args.timeout) as client:
            for sample in samples:
                try:
                    answer, retrieved_contexts, missing_reason = call_love_app_sync(
                        client=client,
                        base_url=args.base_url,
                        question=sample.question,
                        sample_id=sample.sample_id,
                    )
                except Exception as ex:
                    failures += 1
                    answer = ""
                    retrieved_contexts = []
                    missing_reason = f"request_failed: {type(ex).__name__}: {ex}"

                # RAGas requires contexts; if unavailable, fallback to references for compute path.
                effective_contexts = retrieved_contexts if retrieved_contexts else sample.reference_contexts
                context_mode = "retrieved" if retrieved_contexts else "reference_fallback"

                questions.append(sample.question)
                answers.append(answer)
                contexts.append(effective_contexts)
                reference_answers.append(sample.reference_answer)
                reference_contexts.append(sample.reference_contexts)

                per_sample_rows.append(
                    {
                        "id": sample.sample_id,
                        "question": sample.question,
                        "answer": answer,
                        "reference_answer": sample.reference_answer,
                        "retrieved_contexts_count": len(retrieved_contexts),
                        "effective_contexts_count": len(effective_contexts),
                        "context_mode": context_mode,
                        "context_missing_reason": missing_reason if context_mode == "reference_fallback" else "",
                        "tags": "|".join(sample.tags),
                        "dataset_version": sample.dataset_version,
                    }
                )

    if not per_sample_rows:
        print("[ERROR] No valid evaluation samples were collected.", file=sys.stderr)
        return 2

    ragas_dataset = Dataset.from_dict(
        {
            "question": questions,
            "answer": answers,
            "contexts": contexts,
            "ground_truth": reference_answers,
            "reference": reference_answers,
            "ground_truths": reference_contexts,
        }
    )

    # Run selected four metrics.
    result = evaluate(
        ragas_dataset,
        metrics=[faithfulness, answer_relevancy, context_precision, context_recall],
    )

    result_df = result.to_pandas()

    # Merge per-sample rows with metric rows by index alignment.
    metric_columns = [
        "faithfulness",
        "answer_relevancy",
        "context_precision",
        "context_recall",
    ]
    for i, row in enumerate(per_sample_rows):
        if i < len(result_df):
            for c in metric_columns:
                row[c] = safe_float(result_df.iloc[i].get(c))
        else:
            for c in metric_columns:
                row[c] = None

    details_csv = run_dir / "details.csv"
    with details_csv.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(per_sample_rows[0].keys()))
        writer.writeheader()
        writer.writerows(per_sample_rows)

    # Aggregate summary.
    averages: dict[str, float | None] = {}
    gate_checks: dict[str, dict[str, Any]] = {}
    gate_pass = True
    for key in metric_columns:
        values = [row.get(key) for row in per_sample_rows]
        valid = [v for v in values if isinstance(v, float)]
        avg = sum(valid) / len(valid) if valid else None
        averages[key] = avg
        threshold = thresholds[key]
        passed = (avg is not None) and (avg >= threshold)
        gate_checks[key] = {
            "avg": avg,
            "threshold": threshold,
            "passed": passed,
        }
        if args.gate and not passed:
            gate_pass = False

    summary = {
        "run_at": datetime.now().isoformat(timespec="seconds"),
        "base_url": args.base_url,
        "dataset": str(dataset_path),
        "dataset_size": len(samples),
        "request_failures": failures,
        "metrics_avg": averages,
        "thresholds": thresholds,
        "gate_enabled": bool(args.gate),
        "gate_status": "PASS" if (not args.gate or gate_pass) else "FAIL",
        "gate_checks": gate_checks,
        "notes": [
            "Current backend /ai/love_app/chat/sync returns plain string answer.",
            "If retrieved contexts are missing, this run uses reference_fallback contexts when needed.",
        ],
        "artifacts": {
            "details_csv": str(details_csv),
        },
    }
    if captured_jsonl:
        summary["captured_jsonl"] = str(captured_jsonl)

    summary_path = run_dir / "summary.json"
    with summary_path.open("w", encoding="utf-8") as f:
        json.dump(summary, f, ensure_ascii=False, indent=2)

    print(f"[INFO] Evaluation complete. Summary: {summary_path}")
    print(f"[INFO] Details: {details_csv}")
    if args.gate:
        print(f"[INFO] Gate status: {summary['gate_status']}")
        return 0 if summary["gate_status"] == "PASS" else 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
